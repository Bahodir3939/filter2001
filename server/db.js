export const filterProperties = ({ range, bedroom }) => {
  const minPrice = range[0];
  const maxPrice = range[1];

  let filteredData = properties.filter(
    (pro) => pro.price >= minPrice && pro.price <= maxPrice
  );

  if (bedroom !== "any") {
    if (bedroom === "6+") {
      filteredData = filteredData.filter((pro) => pro.bedrooms >= 6);
    } else {
      filteredData = filteredData.filter((pro) => pro.bedrooms === +bedroom);
    }
  }

  return filteredData;
};
